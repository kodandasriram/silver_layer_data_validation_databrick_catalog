WITH source_data_base AS (
SELECT
    a.ID,
    a.TENANT_ID,
    a.IS_ACTIVE,
    a.CREATION_DATE,
    a.LAST_LOGIN,
    a.NAME,
    a.MOBILEPHONE,
    a.EMAIL,
    a.USERNAME,
    a.PASSWORD,
    a.EXTERNAL_ID,
    b.CPR_NUMBER,
    b.DATEOFBIRTH,
    b.GENDER,
    b.NATIONALITY,
    b.LOGINATTEMPTSFAILED,
    b.PHONECOUNTRYCODE,
    b.ISVERIFIED,
    c.NAME AS SELFIEFILENAME,
    d.NAME AS FONTSIDEDOCFILENAME,
    e.NAME AS BACKSIDEDOCFILENAME,
    b.USEOUTSYSTEMSLOGIN,
    b.USEOUTSYSTEMSLOGICSOURCE,
    b.USEOUTSYSTEMSLOGICDATETIME,
    b.OLDPORTAL_TAMKEENIDENTITYID,
    b.ISEMAILVERIFIED,
    b.EMAILVERIFIED_DATETIME,
    b.ISMOBILEVERIFIED,
    b.MOBILEVERIFIED_DATETIME,
    b.ISREJECTED,
    b.REJECTIONREMARK,
    b.SECONDARYPHONENUMBER,
    b.RESETPASSREQUIRED,
    'NEO2' AS SOURCE_SYSTEM_NAME,
    CAST(current_timestamp AT TIME ZONE 'UTC' AS timestamp) AS DBT_UPDATED_AT,
    b.USERID,
    b.SELFIEFILEID,
    b.FONTSIDEDOCFILEID,
    b.BACKSIDEDOCFILEID,
    b.ISFROMOLDPORTAL,
    b.OLDPORTAL_TAMKEENPWDHASH,
    b.OLDPORTAL_TAMKEENPWDSALT,
    b.LASTLOGINOTPVERIFIEDDATETIME,
    b.ISOTPLOCKED,
    b.OTPLOCKEND_DATETIME
FROM dev_iceberg."tmkn-aws-dwh-dev-iceberg-bronze".OSSYS_USER a
LEFT JOIN dev_iceberg."tmkn-aws-dwh-dev-iceberg-bronze".OSUSR_MKZ_USEREXTENSION b
    ON a.ID = b.USERID
LEFT JOIN dev_iceberg."tmkn-aws-dwh-dev-iceberg-bronze".OSUSR_MKZ_USERFILE c
    ON b.SELFIEFILEID = c.ID
LEFT JOIN dev_iceberg."tmkn-aws-dwh-dev-iceberg-bronze".OSUSR_MKZ_USERFILE d
    ON b.FONTSIDEDOCFILEID = d.ID
LEFT JOIN dev_iceberg."tmkn-aws-dwh-dev-iceberg-bronze".OSUSR_MKZ_USERFILE e
    ON b.BACKSIDEDOCFILEID = e.ID
)

SELECT
    CAST(ID AS integer) AS ID,
    CAST(TENANT_ID AS integer) AS TENANT_ID,
    IS_ACTIVE,
    CAST(CREATION_DATE AS timestamp) AS CREATION_DATE,
    CAST(LAST_LOGIN AS timestamp) AS LAST_LOGIN,
    NAME,
    MOBILEPHONE,
    EMAIL,
    USERNAME,
    PASSWORD,
    EXTERNAL_ID,
    CPR_NUMBER,
    CAST(DATEOFBIRTH AS timestamp) AS DATEOFBIRTH,
    GENDER,
    NATIONALITY,
    CAST(LOGINATTEMPTSFAILED AS integer) AS LOGINATTEMPTSFAILED,
    PHONECOUNTRYCODE,
    ISVERIFIED,
    SELFIEFILENAME,
    FONTSIDEDOCFILENAME,
    BACKSIDEDOCFILENAME,
    USEOUTSYSTEMSLOGIN,
    USEOUTSYSTEMSLOGICSOURCE,
    CAST(USEOUTSYSTEMSLOGICDATETIME AS timestamp) AS USEOUTSYSTEMSLOGICDATETIME,
    OLDPORTAL_TAMKEENIDENTITYID,
    ISEMAILVERIFIED,
    CAST(EMAILVERIFIED_DATETIME AS timestamp) AS EMAILVERIFIED_DATETIME,
    ISMOBILEVERIFIED,
    CAST(MOBILEVERIFIED_DATETIME AS timestamp) AS MOBILEVERIFIED_DATETIME,
    ISREJECTED,
    REJECTIONREMARK,
    SECONDARYPHONENUMBER,
    RESETPASSREQUIRED,
    UPPER(TRIM(SOURCE_SYSTEM_NAME)) AS SOURCE_SYSTEM_NAME,
    CAST(DBT_UPDATED_AT AS timestamp) AS DBT_UPDATED_AT,
    CAST(USERID AS integer) AS USERID,
    CAST(SELFIEFILEID AS integer) AS SELFIEFILEID,
    CAST(FONTSIDEDOCFILEID AS integer) AS FONTSIDEDOCFILEID,
    CAST(BACKSIDEDOCFILEID AS integer) AS BACKSIDEDOCFILEID,
    ISFROMOLDPORTAL,
    OLDPORTAL_TAMKEENPWDHASH,
    OLDPORTAL_TAMKEENPWDSALT,
    CAST(LASTLOGINOTPVERIFIEDDATETIME AS timestamp) AS LASTLOGINOTPVERIFIEDDATETIME,
    ISOTPLOCKED,
    CAST(OTPLOCKEND_DATETIME AS timestamp) AS OTPLOCKEND_DATETIME
FROM source_data_base
