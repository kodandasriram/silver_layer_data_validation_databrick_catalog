    A.ISREPLACINGNONBAHRAINI,
    A.EMPLOYEETYPEID,
    A.CONTRACTTYPE,
    CAST(A.CONTRACTSTARTDATE AS date) as CONTRACTSTARTDATE,
    CAST(A.CONTRACTENDDATE AS date) as CONTRACTENDDATE,
    'Neo2' AS SOURCE_SYSTEM_NAME,
    cast(current_timestamp AT TIME ZONE 'UTC' AS timestamp) AS DBT_UPDATED_AT
