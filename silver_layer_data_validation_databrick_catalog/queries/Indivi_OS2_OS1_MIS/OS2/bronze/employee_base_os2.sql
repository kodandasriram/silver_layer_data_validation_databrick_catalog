SELECT
    A.APPLICATIONSUPPORTID,
    A.EMAILADDRESS,
    A.MOBILECOUNTRYPREFIX,
    A.MOBILENUMBER,
    c2.LABEL as ACADEMICDEGREE,
    A.UNIVERSITYNAME,
    C3.COUNTRYNAME as UNIVERSITYLOCATION,
    A.DEGREESPECIALIZATION,
    CAST(A.DATEDEGREEGRADUATION AS date) as DATEDEGREEGRADUATION,
    C4.LABEL as JOBLEVEL,
    A.JOBTITLE,
    A.JOBDEPARTMENT,
    A.JOBRESPONSABILITIES,
    A.JOBCURRENTWAGE,
    C5.LABEL as  SALARYSTATUS,
    CAST(A.JOININGDATE AS date) as JOININGDATE,
    A.CURRENTMONTHSEXPERIENCE,
    A.TOTALMONTHSEXPERIENCE,
    C6.LABEL as EMPLOYMENTTYPE,
    C1.LABEL as EMPLOYEECONTRACTTYPE,
    CAST(A.EMPLOYMENTCONTRACTSTARTDATE AS date) as EMPLOYMENTCONTRACTSTARTDATE,
    CAST(A.EMPLOYMENTCONTRACTENDDATE AS date) as EMPLOYMENTCONTRACTENDDATE,
